// Driver for gate input.

#include "gate_input.h"

namespace braids {

void GateInput::Init() {
  gpio_init(PIN_PULSE1_IN);
  gpio_set_dir(PIN_PULSE1_IN, GPIO_IN);
  gpio_pull_up(PIN_PULSE1_IN);
  previous_state_ = true;
}

}  // namespace braids
